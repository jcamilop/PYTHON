v = input('ingrese un carater: ')
# se selecciona una variable y utilizando enla funcion input para que el uzuario tenga la livertad de ingresar cualquier caracter
if v == 'a' or v == 'A':
    print('v, es vocal')
# la sentencia IF nos ayuda a imprimir solo si la ecuacion se cumple
elif v == 'e' or v == 'E':
    print('v, es vocal')
# La palabra reservada elif es una abreviación de “else if”, y es útil para evitar un sangrado excesivo
elif v == 'i' or v == 'I':
    print('v, es vocal')

elif v == 'o' or v == 'O':
    print('v, es vocal')

elif v == 'u' or v == 'U':
    print('v, es vocal')
    
else:
    print('v, no es vocal')
# else es la otra palabra para decir si la funcion de if o elif no se cumple sacar por consola el resultado final
